#Leer un frase y convertirla a Mayúscula
frase = input("Dime una frase: ")
print("La frase en mayúscula es: ", frase.upper())
print("La frase en minúscula es ", frase.lower())
print("La frase con la primera letra en mayúscula", frase.capitalize())
print("Titulo: ", frase.title())
print("La frase tiene ", len(frase), "letras ")